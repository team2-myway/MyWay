package Menu.dto;

public class SideMenuDto {
	private int m_no;
	private String m_category;
	private String m_name;
	private String m_img;
	private int m_calorie;
	private int m_price;
	private String m_detail;
	public int getM_no() {
		return m_no;
	}
	public void setM_no(int m_no) {
		this.m_no = m_no;
	}
	public String getM_category() {
		return m_category;
	}
	public void setM_category(String m_category) {
		this.m_category = m_category;
	}
	public String getM_name() {
		return m_name;
	}
	public void setM_name(String m_name) {
		this.m_name = m_name;
	}
	public String getM_img() {
		return m_img;
	}
	public void setM_img(String m_img) {
		this.m_img = m_img;
	}
	public int getM_calorie() {
		return m_calorie;
	}
	public void setM_calorie(int m_calorie) {
		this.m_calorie = m_calorie;
	}
	public int getM_price() {
		return m_price;
	}
	public void setM_price(int m_price) {
		this.m_price = m_price;
	}
	public String getM_detail() {
		return m_detail;
	}
	public void setM_detail(String m_detail) {
		this.m_detail = m_detail;
	}
	
	
}

	